package br.com.sisloc.model;

/**
 *
 * @author Guilherme
 */
public class Locacao {

    private int id, idFuncResp, idCliente;

    public Locacao() {
    }

    public Locacao(int id, int idFuncResp, int idCliente) {
        this.id = id;
        this.idFuncResp = idFuncResp;
        this.idCliente = idCliente;
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the idFuncResp
     */
    public int getIdFuncResp() {
        return idFuncResp;
    }

    /**
     * @param idFuncResp the idFuncResp to set
     */
    public void setIdFuncResp(int idFuncResp) {
        this.idFuncResp = idFuncResp;
    }

    /**
     * @return the idCliente
     */
    public int getIdCliente() {
        return idCliente;
    }

    /**
     * @param idCliente the idCliente to set
     */
    public void setIdCliente(int idCliente) {
        this.idCliente = idCliente;
    }

}
